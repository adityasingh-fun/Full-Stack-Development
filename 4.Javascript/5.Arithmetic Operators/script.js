// Arithmetic operators
const a = 20;
const b = 10;

// Addition
const addition = a + b;
console.log('Addition', addition);
// Subtraction
const sub = a - b;
console.log('Subtraction',sub);
// Multiplication 
const mul = a * b;
console.log('Multiplication',mul);
// Division
const div = a / b;
console.log('Division',div);
// Modulo
const mod = a % b;
console.log("Modulus",mod);

let c = 3;
let d = 4;
// Increment Operator
c++;
console.log("c is",c);
d--;
